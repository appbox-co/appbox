import { access, mkdir, writeFile } from "node:fs/promises"
import path from "node:path"
import { fileURLToPath } from "node:url"

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const appRoot = path.resolve(__dirname, "..")

const adminEntrypoint = path.join(appRoot, "admin-module", "src", "entry.tsx")
const bridgePath = path.join(appRoot, "src", "lib", "admin", "bridge.ts")
const availabilityPath = path.join(
  appRoot,
  "src",
  "lib",
  "admin",
  "availability.ts"
)

const bridgeHeader = `// This file is generated by scripts/generate-admin-bridge.mjs.
// Do not edit manually.
`

let bridgeSource = `${bridgeHeader}
import type { ComponentType } from "react"

export const ADMIN_MODULE_AVAILABLE = false as const

export async function loadAdminDashboardModule(): Promise<ComponentType | null> {
  return null
}
`

try {
  await access(adminEntrypoint)
  bridgeSource = `${bridgeHeader}
import type { ComponentType } from "react"

export const ADMIN_MODULE_AVAILABLE = true as const

export async function loadAdminDashboardModule(): Promise<ComponentType> {
  const mod = await import("../../../admin-module/src/entry")
  return mod.default as ComponentType
}
`
} catch {
  // Submodule missing or not buildable from this environment.
}

const availabilitySource = `${bridgeHeader}
export const ADMIN_MODULE_AVAILABLE = ${bridgeSource.includes("= true")} as const
`

await mkdir(path.dirname(bridgePath), { recursive: true })
await writeFile(bridgePath, bridgeSource, "utf8")
await writeFile(availabilityPath, availabilitySource, "utf8")

console.log(
  `[admin-bridge] ${bridgeSource.includes("= true") ? "enabled" : "disabled"}`
)
