import { access, mkdir, writeFile } from "node:fs/promises"
import path from "node:path"
import { fileURLToPath } from "node:url"

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const appRoot = path.resolve(__dirname, "..")

const adminEntrypoint = path.join(appRoot, "admin-module", "src", "entry.tsx")
const bridgePath = path.join(appRoot, "src", "lib", "admin", "bridge.ts")
const availabilityPath = path.join(
  appRoot,
  "src",
  "lib",
  "admin",
  "availability.ts"
)

const bridgeHeader = `// This file is generated by scripts/generate-admin-bridge.mjs.
// Do not edit manually.
`

const isExplicitlyEnabled =
  String(process.env.ENABLE_ADMIN_MODULE ?? "")
    .trim()
    .toLowerCase() === "true" ||
  String(process.env.ENABLE_ADMIN_MODULE ?? "").trim() === "1"

let bridgeSource = `${bridgeHeader}
import type { ComponentType } from "react"

export const ADMIN_MODULE_AVAILABLE = false as const

export interface AdminMenuItem {
  id: string
  label: string
  iconKey: string
  href: string
  children?: AdminMenuItem[]
}

export interface AdminRegistry {
  menu: AdminMenuItem[]
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  pages: { id: string; path: string; component: ComponentType<any> }[]
}

export async function loadAdminModule(): Promise<{
  Shell: ComponentType | null
  registry: AdminRegistry | null
}> {
  return { Shell: null, registry: null }
}
`

if (isExplicitlyEnabled) {
  try {
    await access(adminEntrypoint)
    bridgeSource = `${bridgeHeader}
import type { ComponentType } from "react"

export const ADMIN_MODULE_AVAILABLE = true as const

export interface AdminMenuItem {
  id: string
  label: string
  iconKey: string
  href: string
  children?: AdminMenuItem[]
}

export interface AdminRegistry {
  menu: AdminMenuItem[]
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  pages: { id: string; path: string; component: ComponentType<any> }[]
}

export async function loadAdminModule(): Promise<{
  Shell: ComponentType
  registry: AdminRegistry
}> {
  const mod = await import("../../../admin-module/src/entry")
  return {
    Shell: mod.default as ComponentType,
    registry: mod.adminRegistry as AdminRegistry
  }
}
`
  } catch {
    // Admin module requested but missing/not buildable from this environment.
  }
}

const availabilitySource = `${bridgeHeader}
export const ADMIN_MODULE_AVAILABLE = ${bridgeSource.includes("= true")} as const
`

await mkdir(path.dirname(bridgePath), { recursive: true })
await writeFile(bridgePath, bridgeSource, "utf8")
await writeFile(availabilityPath, availabilitySource, "utf8")

console.log(
  `[admin-bridge] ${bridgeSource.includes("= true") ? "enabled" : "disabled"}`
)
